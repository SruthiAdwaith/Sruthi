package com.user;

public class User {
	
  public String email;
  public String password;
  
	public void setEmail(String str_username)
	{
		email=str_username;	
	}
	public String getEmail()
	{
		return this.email;
	}
	public void setPassword(String str_password)
	{
	
		password=str_password;
	}
	public String getPassword()
	{
		return this.password;
	}
  
  
  
  
  
}
